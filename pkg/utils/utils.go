package utils

import "slices"

func MapKeys[V any](m map[string]V) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	slices.Sort(keys)
	return keys
}

// ToPtr is a helper function to convert a type to a pointer of that same type.
func ToPtr[T any](i T) *T {
	return &i
}
